class ProgramBlk01A {
    public static void main(String[] args) {
        
        // Primitive Types
        int intOne = 123456;
        float floatOne = 123.456f; // remember to put f at end or will throw exception (error)
        double doubleOne = 123.456789;
        char charOne = 'a';
        boolean boolOne = true;

        // Non-reference Types
        String strOne = "Rein";

        System.out.println("===================================");
        System.out.println("      [Primitive Data Types]");
        System.out.println("Integer value: " + intOne);
        System.out.println("Float value: " + floatOne);
        System.out.println("Double value: " + doubleOne);
        System.out.println("Character value: " + charOne);
        System.out.println("Boolean value: " + boolOne);
        System.out.println("===================================");
        System.out.println("      [Non-reference Data Types]");
        System.out.println("String value: " + strOne);
        System.out.println("===================================");

        // Concatenation

    }

}