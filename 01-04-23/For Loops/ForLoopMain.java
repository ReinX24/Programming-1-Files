public class ForLoopMain {

    public static void main(String[] args) {

        /*
         * Print your first name 10 times through incrementing and decrementing for
         * loops.
         * 
         */

        // Incrementing For Loop
        for (int i = 1; i <= 10; i++) {
            System.out.println("Rein");
        }
        System.out.println("**************"); // print line for separation
        // Decrementing For Loop
        for (int i = 10; i >= 1; i--) {
            System.out.println("Rein");
        }

    }

}