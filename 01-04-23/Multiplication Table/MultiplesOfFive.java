public class MultiplesOfFive {

    public static void main(String[] args) {

        /*
         * Printing all the multiples of 5 from 5 to 500 (5 * 1 - 5 * 100)
         * 
         */

        for (int i = 1; i <= 100; i++) {
            System.out.println("5 * " + i + " = " + 5 * i);
        }

    }

}
