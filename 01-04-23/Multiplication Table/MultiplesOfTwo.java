public class MultiplesOfTwo {

    public static void main(String[] args) {

        /*
         * Printing all the multiples of 2 from 2 to 100 (2 * 1 - 2 * 50)
         * 
         */

        for (int i = 1; i <= 50; i++) {

            System.out.println("2 * " + i + " = " + 2 * i);

        }

    }

}