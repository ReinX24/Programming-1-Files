public class PrintHundred {

    public static void main(String[] args) {

        /*
         * Create a program that prints all numbers from 1 to 100 using a while loop.
         * 
         */

        int i = 1;
        while (i <= 100) { // while i is less than or equal to 100
            System.out.println(i); // prints current i value
            i++; // increments i
        }

    }

}
