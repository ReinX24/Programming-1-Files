public class PrintNumWhileLoop {

    public static void main(String[] args) {

        /*
         * Print i as long as i is less than 6. i is equal to 1. Use a while loop to
         * print the results.
         * 
         */

        int i = 1; // i variable & value holder
        while (i < 6) { // while i is less than 6
            System.out.println(i); // prints current i value, prints numbers 1 - 5
            i++; // increments i
        }

    }

}
