public class PrintYesWhile {

    public static void main(String[] args) {

        /*
         * Create a program the will display the String "Yes" 5 times using a while
         * loop.
         * 
         */

        int i = 1; // i and its value
        while (i < 6) { // while i is less than 6
            System.out.println("Yes"); // prints String 5 times, 1 2 3 4 5
            i++; // increments i
        }

    }

}
