public class ForEachLoop {

    public static void main(String[] args) {

        /*
         * Create a For each loop that goes through an array of integers
         * 
         */

        // Creating our array of integers
        int[] numArr = { 1, 3, 5, 7, 9 };

        // For each loop that prints each element in the array
        for (int eachNum : numArr) {

            System.out.println(eachNum); // prints out each element

        }

    }

}