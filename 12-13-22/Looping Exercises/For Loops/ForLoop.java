public class ForLoop {

    public static void main(String[] args) {

        /*
         * Create a for loop that loops the following message 4 times
         * "Foo bar"
         * 
         */

        // Create String that will show our message
        String myMessage = "Foo bar";

        // For loop that repeats the message 4 times
        for (int i = 0; i < 4;i++) {

            System.out.println(myMessage);

        }

    }

}
